package com.revature;

public class MathUtility {

    public double sum(double x, double y) {
        return x + y;
    }

    public double subtract(double x, double y) {
        return x - y;
    }

    public boolean isEven(int x) {
        return x % 2 == 0;
    }

    public boolean isOdd(int x) {
        return x % 2 == 1;
    }

}
